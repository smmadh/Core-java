package selectionstatments;

public class switchcase {

	public static void main(String[] args) {
		int x=10;
		switch(x) {
		case 10:
			System.out.println("hello");
		
		case 20:
			System.out.println("hi");
		
			default:
				System.out.println("world");
			case 21:
				System.out.println("java");
	
		}

	}

}
