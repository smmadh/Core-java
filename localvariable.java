package variables;

public class localvariable {
	

	public static void main(String[] args) {
		int num=10;//local variable
		System.out.println("local variable"+num);
		
		float num1=100f;
		System.out.println("local variable"+num1);
		
		String firstname="madhu";
		String lastname="sudhan";
		int age=21;
		System.out.println(firstname+" "+lastname+" "+age);
		
		char m='A';
		System.out.println("local variable:"+m);
		
		long n=10000l;
		System.out.println("local varibale:"+n);
		

	}

}
